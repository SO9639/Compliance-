// Helper Functions
window.assessmentHelpers = {
  getFrameworkById: function(id) {
    return window.assessmentData.frameworks.find(f => f.id === id);
  },
  
  getItemsByFramework: function(frameworkCode) {
    return window.assessmentData.assessment_items.filter(item => 
      item.framework_code === frameworkCode
    );
  },
  
  calculateScore: function(responses) {
    
    let totalScore = 0;
    let applicableQuestions = 0;
    
    Object.values(responses).forEach(response => {
      if (response !== 'not-applicable') {
        applicableQuestions++;
        totalScore += window.assessmentConfig.scoring[response] || 0;
      }
    });
    
    if (applicableQuestions === 0) return 0;
    return Math.round((totalScore / (applicableQuestions * 100)) * 100);
  },
  
  getComplianceStatus: function(score, frameworkCode) {
    const thresholds = window.assessmentConfig.complianceThresholds;
    const frameworkName = window.assessmentData.frameworks.find(f => f.code === frameworkCode)?.name || frameworkCode;
    
    if (score >= thresholds.compliant) {
      return {
        status: 'COMPLIANT',
        message: frameworkName + ' Status: COMPLIANT',
        class: 'success',
        description: 'Your application meets the compliance requirements.'
      };
    } else if (score >= thresholds.substantially_compliant) {
      return {
        status: 'SUBSTANTIALLY_COMPLIANT', 
        message: frameworkName + ' Status: SUBSTANTIALLY COMPLIANT',
        class: 'warning',
        description: 'Your application is largely compliant but has some gaps to address.'
      };
    } else {
      return {
        status: 'NON_COMPLIANT',
        message: frameworkName + ' Status: NON-COMPLIANT',
        class: 'danger', 
        description: 'Your application has significant compliance gaps that need immediate attention.'
      };
    }
  }
};
